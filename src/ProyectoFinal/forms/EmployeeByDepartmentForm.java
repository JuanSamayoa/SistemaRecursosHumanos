    private void ConsultEmployeeByDepartmentButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConsultEmployeeByDepartmentButtonActionPerformed
        try {
            // FIXED: Use DepartmentReport instead of Reportes
            ArrayList<DepartmentReport> reportData = DepartmentReport.generateDepartmentReport();

            // Create table model
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("Departamento");
            model.addColumn("Cantidad de Empleados");

            // Load data into table
            for (DepartmentReport report : reportData) {
                Object[] row = new Object[2];
                row[0] = report.getDepartmentName();
                row[1] = report.getEmployeeCount();
                model.addRow(row);
            }

            jTable1.setModel(model);

            if (reportData.isEmpty()) {
                JOptionPane.showMessageDialog(this,
                    "No se encontraron departamentos en el sistema.",
                    "Sin resultados",
                    JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this,
                    "Reporte generado exitosamente. Se encontraron " + reportData.size() + " departamentos.",
                    "Reporte completo",
                    JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                "Error al generar el reporte: " + e.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ConsultEmployeeByDepartmentButtonActionPerformed

